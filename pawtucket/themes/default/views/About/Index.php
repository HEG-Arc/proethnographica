			<h2>Un précieux trésor ethnographique</h2>

<p align="justify">En 1942 le P. Wilhelm Schmidt, premier professeur
d’ethnologie à l’Université de Fribourg commença à
rassembler une importante collection ethnographique
issue de diverses sources. Elle comprend plus de 2800
pièces de l’Amérique du Nord, de l’Inde, de la
Papouasie Nouvelle-Guinée, de l’Afrique et de
l’Australie. Suite à un vol, la collection exposée
jusque-là dans les vitrines de la Faculté des Lettres
est restée confinée dans les sous-sols de l’Université.
Il fallait la sauver.</p>
<p align="justify">Du temps de la décolonisation, les activités
scientifiques des missionnaires-ethnologues furent en
grande partie ignorée, n’étant pas considérées comme
politiquement correctes. Pourtant leur histoire
intéresse à nouveau de jeunes chercheurs. Pour faire
connaître ces collections rares et précieuses, nous
avons fondé <b> P r o   E t h n o g r a p h i © a </b>  et
acquis la collection. Ainsi l’avons-nous sauvée !
Notre but est d’abord de faire sortir la collection de
l’oubli en établissant un inventaire électronique. En
outre, nous voulons restaurer et conserver les
nombreuses pièces qui ont souffert de dommages au long
des 50 dernières années et les rendre accessibles aux
intéressés.
</p>
